fun main() {

    val I: Int = 10
    val D: Double = I.toDouble()
    val St: String = "123"
    val S: String = "123.45"
    val In: Int = St.toInt()
    val Do: Double = S.toDouble()

    println("$I is Integer")
    println("$D is Double from Integer")
    println("$St is String")
    println("$In is Integer from String")
    println("$S is String")
    println("$Do is Double from String")

}